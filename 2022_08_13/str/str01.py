# 문자열 자료형

s1 = 'Hello World'
print(type(s1))                 # <class 'str'>
print(s1)

s2 = "Python is fun"
print(type(s2))                 # <class 'str'>
print(s2)

s3 = '''Life is too short, you need python.'''
print(type(s3))                 # <class 'str'>
print(s3)

s4 = """Life is too short, you need python."""
print(type(s4))                 # <class 'str'>
print(s4)

# ", ' 로 앞뒤를 감싸는 거 가능
# :를 붙이지 않아도 됨
# 글자가 한 글자 이상힌 것을 '문자열' 이라고 정의한다.