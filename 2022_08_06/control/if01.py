# 조건문 : if문
# if  조건식 :
#     조건식이 참인 경우에 실행될 문장

if True:
    print('항상 실행1')         # 들여쓰기
    print('항상 실행2')

if False:
    print('실행안됨1')
    print('실행안됨2')
print('무조건 실행') #들여쓰기 하지 않는 것은 위에 조건식에 적용이 안 돼서
# if false에 영향을 안 받아서 실행이 됨

if (3 > 5) :
    print('실행안됨')
print('항상실행3')
print('항상실행4')
#들여쓰기 되지 않는 두 개는 값이 나옴. 위에 3>5는 false라 출력이 안 됨


if 3>5:
    print('호림이 멋있다')
