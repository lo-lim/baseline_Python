# 대입 연산자 : =

a = 10
print('a=', a)

x = y = z = 0
print('x=', x)
print('y=', y)
print('z=', z)

c, d = 10, 20  #c에 10을 할당하고 순서대로 d에 20을 할당하라
print('c=', c)              # c= 10
print('d=', d)              # d= 20


# 값 교환
c, d = d, c
print('c=', c)              # c= 20
print('d=', d)              # d= 10


